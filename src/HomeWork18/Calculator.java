package HomeWork18;

public class Calculator {
    public static double makeSum (double x, double y) {
        return (x + y);
    }
    public static double makeSub (double x, double y) {
        return (x - y);
    }
    public static double makeMul(double x, double y) {
        return (x * y);
    }
    public static double makeDiv (double x, double y) {
        return (x / y);
    }

}
